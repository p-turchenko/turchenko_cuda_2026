! ================================================================
! ЗАДАНИЕ 1 (CUDA): СЛОЖЕНИЕ ДВУХ МАССИВОВ НА GPU
! ================================================================
!
! КЛЮЧЕВЫЕ ПОНЯТИЯ:
!   - CPU/HOST: обычный процессор
!   - GPU/DEVICE: видеокарта
!   - KERNEL: функция, которая бежит на GPU
!   - THREAD: один поток на GPU (один исполнитель)
!   - BLOCK: группа потоков
!   - GRID: группа блоков
!
! ПОРЯДОК ДЕЙСТВИЙ В ПРОГРАММЕ:
!   1. Заполнить массивы A и B на CPU
!   2. Скопировать A и B на GPU
!   3. Запустить ядро на GPU (сложение)
!   4. Скопировать результат обратно на CPU
!   5. Вывести в файл
!
! ================================================================

! ================================================================
! МОДУЛЬ С ЯДРОМ (вынесено отдельно, потому что attributes(global)
! нельзя использовать внутри contains в основной программе)
! ================================================================

module add_kernel_module
    use cudafor
    implicit none
    
contains

    ! ================================================================
    ! ЯДРО (KERNEL) - ЭТА ФУНКЦИЯ БЕЖИТ НА GPU
    ! ================================================================
    
    attributes(global) subroutine add_kernel(a, b, c, m)
        real, device, intent(in)  :: a(:)
        real, device, intent(in)  :: b(:)
        real, device, intent(out) :: c(:)
        integer, value, intent(in) :: m
        
        integer :: i
        
        i = (blockIdx%x - 1) * blockDim%x + threadIdx%x
        
        if (i <= m) then
            c(i) = a(i) + b(i)
        end if
        
    end subroutine add_kernel

end module add_kernel_module

! ================================================================
! ОСНОВНАЯ ПРОГРАММА (ВЫПОЛНЯЕТСЯ НА CPU)
! ================================================================
program add_arrays_cuda
    use cudafor
    use add_kernel_module
    implicit none
    
    integer, parameter :: n = 10
    integer, parameter :: out_unit = 20    ! Номер файла для вывода
    
    real, dimension(n) :: A, B, C
    real, device, dimension(n) :: A_d, B_d, C_d
    
    integer :: i, istat
    
    ! ------------------------------------------------------------
    ! ШАГ 1: ЗАПОЛНЯЕМ МАССИВЫ НА CPU
    ! ------------------------------------------------------------
    
    call random_seed()
    call random_number(A)
    call random_number(B)
    
    ! ------------------------------------------------------------
    ! ШАГ 2: КОПИРУЕМ ДАННЫЕ С CPU НА GPU (H2D)
    ! ------------------------------------------------------------
    
    A_d = A
    B_d = B
    
    ! ------------------------------------------------------------
    ! ШАГ 3: ЗАПУСК ЯДРА НА GPU
    ! ------------------------------------------------------------
    
    call add_kernel<<<dim3(1,1,1), dim3(n,1,1)>>>(A_d, B_d, C_d, n)
    
    ! ------------------------------------------------------------
    ! ШАГ 4: СИНХРОНИЗАЦИЯ (ЖДЁМ ЗАВЕРШЕНИЯ GPU)
    ! ------------------------------------------------------------
    
    istat = cudaDeviceSynchronize()
    
    ! ------------------------------------------------------------
    ! ШАГ 5: КОПИРУЕМ РЕЗУЛЬТАТ С GPU НА CPU (D2H)
    ! ------------------------------------------------------------
    
    C = C_d
    
    ! ------------------------------------------------------------
    ! ШАГ 6: ВЫВОД РЕЗУЛЬТАТОВ В ФАЙЛ
    ! ------------------------------------------------------------
    
    open(unit=out_unit, file='output.txt', status='replace', action='write')
    
    write(out_unit, '(A)') '=== СЛОЖЕНИЕ МАССИВОВ НА GPU ==='
    write(out_unit, '(A)') ''
    write(out_unit, '(A)') 'КОНФИГУРАЦИЯ ЗАПУСКА:'
    write(out_unit, '(A,I0)') '  Размер массивов: n = ', n
    write(out_unit, '(A)') '  grid  = dim3(1, 1, 1)  ! 1 блок'
    write(out_unit, '(A,I0,A)') '  block = dim3(', n, ', 1, 1)  ! n потоков'
    write(out_unit, '(A)') ''
    
    write(out_unit, '(A)') 'Массив A:'
    do i = 1, n
        write(out_unit, '(I4,F12.6)') i, A(i)
    end do
    
    write(out_unit, '(A)') ''
    write(out_unit, '(A)') 'Массив B:'
    do i = 1, n
        write(out_unit, '(I4,F12.6)') i, B(i)
    end do
    
    write(out_unit, '(A)') ''
    write(out_unit, '(A)') 'Результат C = A + B (вычислено на GPU):'
    do i = 1, n
        write(out_unit, '(I4,F12.6)') i, C(i)
    end do
    
    write(out_unit, '(A)') ''
    write(out_unit, '(A)') '=== ВСЁ ВЫПОЛНЕНО УСПЕШНО ==='
    
    close(out_unit)
    
    ! Дублируем вывод на экран
    print *, 'Результаты записаны в файл output.txt'
    print *, '=== ВСЁ ВЫПОЛНЕНО УСПЕШНО ==='

end program add_arrays_cuda
